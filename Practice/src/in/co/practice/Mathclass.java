package in.co.practice;

public class Mathclass {
	public static void main(String[]args) {
		System.out.println("Mathematics Operation");
		System.out.println("Max 2,5 :" +Math.max(2, 5));
		System.out.println("Min 10,15 :" +Math.min(10, 15));
		System.out.println("Absolute3.7 :"+ Math.abs(3.7));
		System.out.println("Exp10  : "+ Math.exp(10));
		System.out.println("Random Number : " + Math.random()*100);
		System.out.println("Square Root : "+Math.sqrt(5));
	}
}
