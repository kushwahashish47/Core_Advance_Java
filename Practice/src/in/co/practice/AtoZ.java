package in.co.practice;

public class AtoZ {
	public static void main (String[]args) {
		
		for(char i= 'A' ; i<= 'Z' ; i++) {    // to print a to z , inverted commas are must
			System.out.print(i+" ");
		}
		for(char i= 'a' ; i<= 'f' ; i++) {
			System.out.println();
			System.out.print(""+i);
	}
	}
}
